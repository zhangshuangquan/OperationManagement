//鼠标锁定
         $("#fName").focus(function(){
        	 
        	 //获得当前的值
        	 $currVal=$(this).val();
        	 if($currVal==this.defaultValue){
        		 $(this).val(""); 	 
        	 }else{
        		 $(this).val($currVal);
        	 }
          }	 
         )
         
         //鼠标移出
         $("#fName").blur(function(){
        	 
        	 //获得当前的值
        	 $currVal=$(this).val();
        	 if($currVal==""){
        		 $(this).val(this.defaultValue); 	 
        	 }else{
        		 $(this).val($currVal);
        	 }
          }	 
         )
         
         
