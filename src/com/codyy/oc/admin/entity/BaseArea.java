package com.codyy.oc.admin.entity;

import com.codyy.commons.annotation.ExcelColumn;


/**
 * 地区表
 * @author zhangshuangquan
 *
 */
public class BaseArea {
	
    private String baseAreaId;        //区域Id

    private String areaName;          //区域名称

    private String areaCode;          //区域编码

    private Integer sort;

    private String parentId;       
    @ExcelColumn(columnName="区域")
    private String areaPath;           //区域地址
    
    private String areaIdPath;
    
    private Integer areaLevel;          //区域等级

    public String getBaseAreaId() {
        return baseAreaId;
    }

    public void setBaseAreaId(String baseAreaId) {
        this.baseAreaId = baseAreaId;
    }

    public String getAreaName() {
        return areaName;
    }

    public void setAreaName(String areaName) {
        this.areaName = areaName;
    }

    public String getAreaCode() {
        return areaCode;
    }

    public void setAreaCode(String areaCode) {
        this.areaCode = areaCode;
    }

    public Integer getSort() {
        return sort;
    }

    public void setSort(Integer sort) {
        this.sort = sort;
    }

    public String getParentId() {
        return parentId;
    }

    public void setParentId(String parentId) {
        this.parentId = parentId;
    }

    public Integer getAreaLevel() {
        return areaLevel;
    }

    public void setAreaLevel(Integer areaLevel) {
        this.areaLevel = areaLevel;
    }

	public String getAreaPath() {
		return areaPath;
	}

	public void setAreaPath(String areaPath) {
		this.areaPath = areaPath;
	}

	public String getAreaIdPath() {
		return areaIdPath;
	}

	public void setAreaIdPath(String areaIdPath) {
		this.areaIdPath = areaIdPath;
	}

}