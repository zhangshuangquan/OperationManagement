package com.codyy.oc.admin.entity;

public class AdminPermission {
    private String adminPermissionId;

	private String adminMenuId;

	private String permissionName;

	private String groupName;

	private String permissionDesc;

	public String getAdminPermissionId() {
		return adminPermissionId;
	}

	public void setAdminPermissionId(String adminPermissionId) {
		this.adminPermissionId = adminPermissionId;
	}

	public String getAdminMenuId() {
		return adminMenuId;
	}

	public void setAdminMenuId(String adminMenuId) {
		this.adminMenuId = adminMenuId;
	}

	public String getPermissionName() {
		return permissionName;
	}

	public void setPermissionName(String permissionName) {
		this.permissionName = permissionName;
	}

	public String getGroupName() {
		return groupName;
	}

	public void setGroupName(String groupName) {
		this.groupName = groupName;
	}

	public String getPermissionDesc() {
		return permissionDesc;
	}

	public void setPermissionDesc(String permissionDesc) {
		this.permissionDesc = permissionDesc;
	}
	
	public AdminPermission(){}//反射调用

	public AdminPermission(String adminPermissionId, String adminMenuId,
			String permissionName, String groupName, String permissionDesc) {
		super();
		this.adminPermissionId = adminPermissionId;
		this.adminMenuId = adminMenuId;
		this.permissionName = permissionName;
		this.groupName = groupName;
		this.permissionDesc = permissionDesc;
	}

	@Override
	public String toString() {
		return "AdminPermission [adminPermissionId=" + adminPermissionId
				+ ", adminMenuId=" + adminMenuId + ", permissionName="
				+ permissionName + ", groupName=" + groupName
				+ ", permissionDesc=" + permissionDesc + ", toString()="
				+ super.toString() + "]";
	}
	
	

}