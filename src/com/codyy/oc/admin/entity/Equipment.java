package com.codyy.oc.admin.entity;

import java.util.Date;

public class Equipment {

	
	private String equipment_delivery_id;
	private String maintenance_order_id;
	private String name;
	private Date delivery_time;
	private String expressCompany;
	private String expressNum;

	public String getExpressCompany() {
		return expressCompany;
	}
	public void setExpressCompany(String expressCompany) {
		this.expressCompany = expressCompany;
	}
	public String getExpressNum() {
		return expressNum;
	}
	public void setExpressNum(String expressNum) {
		this.expressNum = expressNum;
	}
	public String getEquipment_delivery_id() {
		return equipment_delivery_id;
	}
	public void setEquipment_delivery_id(String equipment_delivery_id) {
		this.equipment_delivery_id = equipment_delivery_id;
	}
	public String getMaintenance_order_id() {
		return maintenance_order_id;
	}
	public void setMaintenance_order_id(String maintenance_order_id) {
		this.maintenance_order_id = maintenance_order_id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Date getDelivery_time() {
		return delivery_time;
	}
	public void setDelivery_time(Date delivery_time) {
		this.delivery_time = delivery_time;
	}
	
	
}
