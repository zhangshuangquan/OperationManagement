package com.codyy.oc.admin.entity;

import java.util.ArrayList;
import java.util.List;

public class RegRProject {
    private String regRProjectId;

    private String projectId;

    private String regionalResourceId;
    
	
	private List<String> projectIdList = new ArrayList<String>();//记录一个用户对应多个项目的id

	public List<String> getProjectIdList() {
		return projectIdList;
	}
	public void setProjectIdList(List<String> projectIdList) {
		this.projectIdList = projectIdList;
	}

    public String getRegRProjectId() {
        return regRProjectId;
    }

    public void setRegRProjectId(String regRProjectId) {
        this.regRProjectId = regRProjectId;
    }

    public String getProjectId() {
        return projectId;
    }

    public void setProjectId(String projectId) {
        this.projectId = projectId;
    }

    public String getRegionalResourceId() {
        return regionalResourceId;
    }

    public void setRegionalResourceId(String regionalResourceId) {
        this.regionalResourceId = regionalResourceId;
    }
	@Override
	public String toString() {
		return "RegRProject [regRProjectId=" + regRProjectId + ", projectId="
				+ projectId + ", regionalResourceId=" + regionalResourceId
				+ ", projectIdList=" + projectIdList + ", toString()="
				+ super.toString() + "]";
	}
    
    
}