package com.codyy.oc.admin;

public class AppConfig {
	private String transferChars;
	private String onlineClassUrl;

	public String getTransferChars() {
		return transferChars;
	}

	public void setTransferChars(String transferChars) {
		this.transferChars = transferChars;
	}

	public String getOnlineClassUrl() {
		return onlineClassUrl;
	}

	public void setOnlineClassUrl(String onlineClassUrl) {
		this.onlineClassUrl = onlineClassUrl;
	}
}
