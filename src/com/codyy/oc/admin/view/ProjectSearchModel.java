package com.codyy.oc.admin.view;

/**
 * 项目查询模板
 * @author zhangshuangquan
 *
 */
public class ProjectSearchModel {

	private String areaId;
	private String projectName;
	private String managerName;
	private String schoolName;
	private String explorationDesc;
	private String installDesc;
	private String updateDesc;
	public String getAreaId() {
		return areaId;
	}
	public void setAreaId(String areaId) {
		this.areaId = areaId;
	}
	public String getProjectName() {
		return projectName;
	}
	public void setProjectName(String projectName) {
		this.projectName = projectName;
	}
	
	public String getManagerName() {
		return managerName;
	}
	public void setManagerName(String managerName) {
		this.managerName = managerName;
	}
	public String getSchoolName() {
		return schoolName;
	}
	public void setSchoolName(String schoolName) {
		this.schoolName = schoolName;
	}
	public String getExplorationDesc() {
		return explorationDesc;
	}
	public void setExplorationDesc(String explorationDesc) {
		this.explorationDesc = explorationDesc;
	}
	public String getInstallDesc() {
		return installDesc;
	}
	public void setInstallDesc(String installDesc) {
		this.installDesc = installDesc;
	}
	public String getUpdateDesc() {
		return updateDesc;
	}
	public void setUpdateDesc(String updateDesc) {
		this.updateDesc = updateDesc;
	}
	
}
