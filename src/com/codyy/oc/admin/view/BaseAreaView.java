package com.codyy.oc.admin.view;

import com.codyy.oc.admin.entity.BaseArea;

public class BaseAreaView extends BaseArea {

	//是否有子节点
	private Integer hasChilder;
	
	//是否有直属校
	private Integer hasDirectlySchool;
	
	//级别对应的名称
	private String levelName;

	public Integer getHasChilder() {
		return hasChilder;
	}

	public void setHasChilder(Integer hasChilder) {
		this.hasChilder = hasChilder;
	}

	public String getLevelName() {
		return levelName;
	}

	public void setLevelName(String levelName) {
		this.levelName = levelName;
	}

	public Integer getHasDirectlySchool() {
		return hasDirectlySchool;
	}

	public void setHasDirectlySchool(Integer hasDirectlySchool) {
		this.hasDirectlySchool = hasDirectlySchool;
	}

}
