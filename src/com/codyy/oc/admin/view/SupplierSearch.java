package com.codyy.oc.admin.view;

public class SupplierSearch {

	private String baseAreaId;
	private String name;
	private String qualification;
	private String leftData;
	private String rightData;
	private String contactName;
	private String contactPhone;
	public String getBaseAreaId() {
		return baseAreaId;
	}
	public void setBaseAreaId(String baseAreaId) {
		this.baseAreaId = baseAreaId;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getQualification() {
		return qualification;
	}
	public void setQualification(String qualification) {
		this.qualification = qualification;
	}
	public String getLeftData() {
		return leftData;
	}
	public void setLeftData(String leftData) {
		this.leftData = leftData;
	}
	public String getRightData() {
		return rightData;
	}
	public void setRightData(String rightData) {
		this.rightData = rightData;
	}
	public String getContactName() {
		return contactName;
	}
	public void setContactName(String contactName) {
		this.contactName = contactName;
	}
	public String getContactPhone() {
		return contactPhone;
	}
	public void setContactPhone(String contactPhone) {
		this.contactPhone = contactPhone;
	}
	@Override
	public String toString() {
		return "SupplierSearch [baseAreaId=" + baseAreaId + ", name=" + name
				+ ", qualification=" + qualification + ", leftData=" + leftData
				+ ", rightData=" + rightData + ", contactName=" + contactName
				+ ", contactPhone=" + contactPhone + ", toString()="
				+ super.toString() + "]";
	}
	
	
}
