package com.codyy.oc.admin.view;

public class RegionalResourceSearch {

	private String baseAreaId;
	private String name;
	private String type;
	private String contactPersonName;
	private String contactPersonPhone;
	private String projectName;
	
	public String getProjectName() {
		return projectName;
	}
	public void setProjectName(String projectName) {
		this.projectName = projectName;
	}
	public String getBaseAreaId() {
		return baseAreaId;
	}
	public void setBaseAreaId(String baseAreaId) {
		this.baseAreaId = baseAreaId;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getContactPersonName() {
		return contactPersonName;
	}
	public void setContactPersonName(String contactPersonName) {
		this.contactPersonName = contactPersonName;
	}
	public String getContactPersonPhone() {
		return contactPersonPhone;
	}
	public void setContactPersonPhone(String contactPersonPhone) {
		this.contactPersonPhone = contactPersonPhone;
	}
	@Override
	public String toString() {
		return "RegionalResourceSearch [baseAreaId=" + baseAreaId + ", name="
				+ name + ", type=" + type + ", contactPersonName="
				+ contactPersonName + ", contactPersonPhone="
				+ contactPersonPhone + ", toString()=" + super.toString() + "]";
	}
	
	
}
